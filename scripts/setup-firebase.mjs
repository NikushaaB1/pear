#!/usr/bin/env node
/**
 * Firebase სრული სეტაპი — გაუშვი: npx firebase-tools@latest login && node scripts/setup-firebase.mjs
 */
import { execSync } from 'node:child_process'
import { writeFileSync, readFileSync, existsSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')
const projectId = process.env.FIREBASE_PROJECT_ID || 'pear-elite-platform'
const fb = (cmd) => execSync(`npx -y firebase-tools@latest ${cmd}`, { cwd: root, stdio: 'pipe', encoding: 'utf8' })

function log(msg) {
  console.log(`\n✓ ${msg}`)
}

try {
  fb('projects:list')
  log('Firebase ავტორიზაცია OK')
} catch {
  console.error('\n✗ ჯერ გაეშვი: npx firebase-tools@latest login')
  process.exit(1)
}

try {
  fb(`projects:create ${projectId} --display-name "PEAR Elite"`)
  log(`პროექტი შეიქმნა: ${projectId}`)
} catch {
  log(`პროექტი უკვე არსებობს: ${projectId}`)
}

fb(`use ${projectId}`)
log('აქტიური პროექტი დაყენებულია')

try {
  fb('deploy --only firestore:rules,storage')
  log('Firestore + Storage წესები გაშვებულია')
} catch (e) {
  console.warn('წესების გაშვება ვერ მოხერხდა — Firestore/Storage ჩართე Console-ში')
}

let sdk
try {
  const apps = fb(`apps:list WEB --json`)
  const parsed = JSON.parse(apps)
  const webApp = parsed.result?.find((a) => a.platform === 'WEB')
  if (webApp) {
    sdk = JSON.parse(fb(`apps:sdkconfig WEB ${webApp.appId} --json`))
  }
} catch {
  /* create below */
}

if (!sdk) {
  try {
    fb(`apps:create WEB "PEAR Elite Web"`)
    sdk = JSON.parse(fb('apps:sdkconfig WEB --json'))
  } catch (e) {
    console.error('Web app შექმნა ვერ მოხერხდა:', e.message)
    process.exit(1)
  }
}

const cfg = sdk.result?.sdkConfig || sdk.sdkConfig || sdk
const env = `# Auto-generated by scripts/setup-firebase.mjs
VITE_FIREBASE_API_KEY=${cfg.apiKey || ''}
VITE_FIREBASE_AUTH_DOMAIN=${cfg.authDomain || ''}
VITE_FIREBASE_PROJECT_ID=${cfg.projectId || projectId}
VITE_FIREBASE_STORAGE_BUCKET=${cfg.storageBucket || ''}
VITE_FIREBASE_MESSAGING_SENDER_ID=${cfg.messagingSenderId || ''}
VITE_FIREBASE_APP_ID=${cfg.appId || ''}
`
writeFileSync(resolve(root, '.env'), env)
log('.env ფაილი შეიქმნა')

console.log(`
══════════════════════════════════════════
  Firebase სეტაპი დასრულებულია!
  გადატვირთე: npm run dev

  Console → Authentication → Email/Password → Enable
  Console → Firestore → Create database
  Console → Storage → Get started
══════════════════════════════════════════
`)
